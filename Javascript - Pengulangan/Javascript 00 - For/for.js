// Menghitung urutan nomor dari yang kecil hingga besar
// Increment
// hitung++
for (hitung = 1; hitung <= 10; hitung++){
    document.write("Urutan ke- " + hitung + "<br>");
}

// Menghitung urutan nomor dari yang besar hingga kecil
// Decrement
// hitungMundur--
for (hitungMundur = 10; hitungMundur >= 1; hitungMundur--){
    document.write("Perulangan ke- " + hitungMundur + "<br>")
}

// Menghitung urutan kelipatan 2 atau berapapun
for (tambahDua = 0; tambahDua <= 20; tambahDua+=2){
    document.write("Urutan ke- " + tambahDua + "<br>")
}